#include"colorname.h"

void SelectColor(int* objColor, int whatColor) {

	if (whatColor == 0)
		*objColor = Amethyst;

	if (whatColor == 1)
		*objColor = Crocus;

	if (whatColor == 2)
		*objColor = Lavender;

	if (whatColor == 3)
		*objColor = Raspberry;

	if (whatColor == 4)
		*objColor = Purple;

	if (whatColor == 5)
		*objColor = Ayame;

	if (whatColor == 6)
		*objColor = Hujimurasaki;

	if (whatColor == 7)
		*objColor = Hutaai;

	if (whatColor == 8)
		*objColor = Hasita;

	if (whatColor == 9)
		*objColor = Kodaimurasaki;

	if (whatColor == 10)
		*objColor = Kousi;

	if (whatColor == 11)
		*objColor = Pansy;

}